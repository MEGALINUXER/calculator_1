"""Модуль содержит функцию произведения."""


def multiplication(num_1: int, num_2: int) -> int:
    """Функция принимает два числа и возвращает их произведение

    Args:
        num_1 (int): первое число
        num_2 (int): второе число

    Returns:
        int: произведение двух чисел
    """
    return num_1 * num_2  # Возвращаем результат умножения
