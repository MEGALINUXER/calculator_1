"""Модуль содержит функцию сложения."""


def addition(num1: int, num2: int) -> int:
    """
    The function `addition` takes two integer inputs and returns their sum.

    Args:
      num1 (int): The parameter `num1` is an integer that represents the first
      number to be added in the
    addition function.
      num2 (int): num2 is the second integer number that will be added to num1
      in the addition function.

    Returns:
      The function `addition` is returning the sum of `num1` and `num2`.
    """
    return num1 + num2
